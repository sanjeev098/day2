package com.capg.cal;

public class vowel {
	public static void main(String[]args) {
		char ch='a';
		if(ch=='a'||ch=='e'||ch=='i'||ch=='o'||ch=='u')
		{
			System.out.println("character'"+ch+"' a vowel");
		} 
		else
		{
			System.out.println("character'"+ch+"'is a consonant");
			
		}
		
		
	}

}
